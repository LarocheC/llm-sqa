"""
Single source of truth for every path the experiments touch.

Everything is env-var overridable so the pipeline runs on a machine that is not the
one it was developed on. Defaults assume the layout described in REPRODUCING.md.

    SQA_ROOT           repo root                     (default: inferred from this file)
    SQA_DATA_ROOT      where the big corpora live    (default: ~/data)
    SQA_LIBRITTS_ROOT  LibriTTS-R root               (default: $SQA_DATA_ROOT/libritts_r/LibriTTS_R)
    SQA_MUSAN_ROOT     MUSAN root                    (default: $SQA_DATA_ROOT/musan)
    SQA_RIR_ROOT       measured RIR root     (default: $SQA_DATA_ROOT/RIRs/rirs)
    SQA_RESULTS_DIR    results/artifacts             (default: $SQA_ROOT/experiments/results)
    SQA_WORK_DIR       scratch audio (was /tmp/*)    (default: $SQA_ROOT/experiments/work)
    SQA_NISQA_DIR      NISQA clone                   (default: $SQA_ROOT/experiments/NISQA)
    SQA_CKPT_V3_DIR    v3 checkpoints                (default: $SQA_RESULTS_DIR/planb/ckpt_v3)

Rule of thumb: never hardcode a path in an experiment script — import it from here.
Missing inputs raise a clear, actionable error instead of an IndexError deep in a glob.
"""

import glob
import os
from pathlib import Path

# --------------------------------------------------------------------------- roots
SQA_ROOT = Path(os.environ.get("SQA_ROOT", Path(__file__).resolve().parent.parent))
# salmonn_core.py + the OmegaConf ${oc.env:SQA_ROOT} interpolation both need this set.
os.environ.setdefault("SQA_ROOT", str(SQA_ROOT))


def _env_path(var: str, default: Path) -> Path:
    return Path(os.environ.get(var, str(default))).expanduser()


def _first_existing(candidates: list[Path], fallback: Path) -> Path:
    """Tolerate the couple of layouts a dataset commonly unpacks into."""
    for c in candidates:
        if c.expanduser().exists():
            return c
    return fallback


DATA_ROOT = _env_path("SQA_DATA_ROOT", Path("~/data"))

LIBRITTS_ROOT = _env_path("SQA_LIBRITTS_ROOT", DATA_ROOT / "libritts_r" / "LibriTTS_R")
# the MUSAN tarball unpacks to musan/, but a `musan_dl/musan` nesting is common too
MUSAN_ROOT = _env_path(
    "SQA_MUSAN_ROOT",
    _first_existing([DATA_ROOT / "musan", DATA_ROOT / "musan_dl" / "musan"], DATA_ROOT / "musan"),
)
RIR_ROOT = _env_path("SQA_RIR_ROOT", DATA_ROOT / "RIRs" / "rirs")

RESULTS_DIR = _env_path("SQA_RESULTS_DIR", SQA_ROOT / "experiments" / "results")
PLANB_DIR = RESULTS_DIR / "planb"
# Scratch audio that used to be written to /tmp. It was NOT safe there: the
# enhancement eval reads WAVs that a *different* script wrote, so a /tmp clean
# silently produced a stale/empty eval instead of a clean failure.
WORK_DIR = _env_path("SQA_WORK_DIR", SQA_ROOT / "experiments" / "work")

NISQA_DIR = _env_path("SQA_NISQA_DIR", SQA_ROOT / "experiments" / "NISQA")
NISQA_WEIGHTS = NISQA_DIR / "weights" / "nisqa.tar"
NISQA_PREDICT = NISQA_DIR / "run_predict.py"

CKPT_V3_DIR = _env_path("SQA_CKPT_V3_DIR", PLANB_DIR / "ckpt_v3")
HF_CKPT_REPO = os.environ.get("SQA_HF_CKPT_REPO", "claroche1/salmonn-sqa-planb-v3")

# The corpus WAVs generated by generate_corpus.py (regenerable; not shipped).
CORPUS_WAV_DIR = _env_path("SQA_CORPUS_WAV_DIR", DATA_ROOT / "planb_corpus")

# The measured RIR bank, as a glob (room_*_RT_*/inroom_*/mic_0.wav under RIR_ROOT).
RIR_GLOB = str(RIR_ROOT / "room_*_RT_*" / "inroom_*" / "mic_0.wav")

SR = 16000


# --------------------------------------------------------------------------- helpers
class MissingInput(FileNotFoundError):
    """A required dataset/model/artifact is not on this machine."""


def require(path, what: str, how_to_get: str) -> Path:
    """Return `path` if it exists, else raise with instructions. Use this instead of
    letting a glob(...)[0] blow up with an opaque IndexError."""
    p = Path(path)
    if not p.exists():
        raise MissingInput(
            f"\n  Missing {what}: {p}\n"
            f"  How to get it: {how_to_get}\n"
            f"  (Override the location with the matching SQA_* env var — see experiments/config.py)\n"
        )
    return p


def voicebank_parquet() -> Path:
    """The VoiceBank-DEMAND-16k test parquet from the HF cache.

    This glob was copy-pasted into 11 scripts, each doing `glob(...)[0]` — an
    IndexError if the dataset was never downloaded. One place, one clear error.
    """
    override = os.environ.get("SQA_VOICEBANK_PARQUET")
    if override:
        return require(override, "VoiceBank-DEMAND parquet (SQA_VOICEBANK_PARQUET)",
                       "point it at the test-*.parquet file")
    pattern = os.path.expanduser(
        "~/.cache/huggingface/hub/datasets--JacobLinCool--VoiceBank-DEMAND-16k/"
        "snapshots/*/data/test-00000-of-00001.parquet"
    )
    hits = sorted(glob.glob(pattern))
    if not hits:
        raise MissingInput(
            "\n  Missing the VoiceBank-DEMAND-16k test split (HF cache).\n"
            "  How to get it:\n"
            "    huggingface-cli download JacobLinCool/VoiceBank-DEMAND-16k "
            "--repo-type dataset\n"
            "  Or set SQA_VOICEBANK_PARQUET to an existing test parquet.\n"
        )
    return Path(hits[0])


def rir_bank() -> list[str]:
    """Paths of the measured RIRs. Clear error if the bank is absent."""
    hits = sorted(glob.glob(RIR_GLOB))
    if not hits:
        raise MissingInput(
            f"\n  Missing the measured RIR bank (looked for {RIR_GLOB}).\n"
            "  How to get it: download the measured RIRs and set SQA_RIR_ROOT.\n"
        )
    return hits


def ckpt_v3(stage: int = 2) -> Path:
    """Resolve a v3 checkpoint. Prefers the downloaded HF copy, falls back to a local
    training run. Raises with the fetch command rather than an IndexError."""
    name = f"stage{stage}_checkpoint_best.pth"
    downloaded = CKPT_V3_DIR / name
    if downloaded.exists():
        return downloaded
    # a local training run (experiments/results/planb/ckpt_stage{N}_v3/<ts>/checkpoint_best.pth)
    local = sorted(glob.glob(str(PLANB_DIR / f"ckpt_stage{stage}_v3" / "*" / "checkpoint_best.pth")))
    if local:
        return Path(local[-1])
    raise MissingInput(
        f"\n  Missing the Plan B v3 stage-{stage} checkpoint.\n"
        f"  How to get it:  uv run python scripts/fetch_checkpoints.py\n"
        f"  (downloads {HF_CKPT_REPO} into {CKPT_V3_DIR})\n"
        f"  Or train it yourself: experiments/planb/train/run_v3.sh\n"
    )


def work_dir(name: str) -> Path:
    """A scratch subdir under WORK_DIR (created on demand)."""
    p = WORK_DIR / name
    p.mkdir(parents=True, exist_ok=True)
    return p


def results_dir(*parts) -> Path:
    p = RESULTS_DIR.joinpath(*parts)
    p.mkdir(parents=True, exist_ok=True)
    return p
